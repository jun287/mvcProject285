package kr.or.smartTiger.dto;

public class Engineer {
	
	private int engineerNo;
	private String engineerName;
	private String engineerPhoneNo;
	private String engineerEmail;
	private String enginnerCareer;
	private String enginnerQualification;
	
	public int getEngineerNo() {
		return engineerNo;
	}
	public void setEngineerNo(int engineerNo) {
		this.engineerNo = engineerNo;
	}
	public String getEngineerName() {
		return engineerName;
	}
	public void setEngineerName(String engineerName) {
		this.engineerName = engineerName;
	}
	public String getEngineerPhoneNo() {
		return engineerPhoneNo;
	}
	public void setEngineerPhoneNo(String engineerPhoneNo) {
		this.engineerPhoneNo = engineerPhoneNo;
	}
	public String getEngineerEmail() {
		return engineerEmail;
	}
	public void setEngineerEmail(String engineerEmail) {
		this.engineerEmail = engineerEmail;
	}
	public String getEnginnerCareer() {
		return enginnerCareer;
	}
	public void setEnginnerCareer(String enginnerCareer) {
		this.enginnerCareer = enginnerCareer;
	}
	public String getEnginnerQualification() {
		return enginnerQualification;
	}
	public void setEnginnerQualification(String enginnerQualification) {
		this.enginnerQualification = enginnerQualification;
	}
	
	
}	
